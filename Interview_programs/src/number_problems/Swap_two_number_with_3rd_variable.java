package number_problems;

public class Swap_two_number_with_3rd_variable {

	public static void main(String[] args) {

		int a=10,b=9,temp;
		
		System.out.println("before Swaping:"+ a +"\n "+  b);
		
		temp=a;
		a=b;
		b=temp;
		System.out.println("After swaping" + a + "\n "+  b);
		
	}

}
